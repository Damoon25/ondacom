                            <span class="fw-bold">1. OBJETIVO</span><br>
                            Este documento estrutura a Política de Prevenção e Enfrentamento ao Assédio, e a Discriminação, e estabelece estratégias
                            para criar e manter um ambiente seguro e respeitoso para as pessoas relacionadas a ONDACOM de modo a protegê-los de um local
                            de trabalho intimidador, ofensivo ou hostil. <br><br>

                            <span class="fw-bold">2. ABRANGÊNCIA</span><br>
                            Esta política de prevenção ao assédio, e a discriminação aplica-se a todas as filiais da ONDACOM e seus respectivos
                            colaboradores, independentemente das atividades que desempenhem cargo ou função que ocupem, e unidade que estejam alocados.
                            Os colaboradores da ONDACOM, ainda podem confiar nesta política caso sofram discriminação ou assédio
                            causado por terceiros em sua relação com a empresa <br><br>

                            <span class="fw-bold">3. DEFINIÇÕES</span><br>

                            3.1) Local de trabalho <br>
                            Tanto o assédio quanto a discriminação são proibidos no local de trabalho do colaborador, ou qualquer outro lugar
                            que possa ser considerado uma extensão do espaço de trabalho, incluindo banheiros, acomodações quando fornecidas
                            pela ONDACOM, eventos sociais relacionados ao trabalho, festas e eventos da empresa, qualquer meio de transporte
                            fornecidos para locomoção a qualquer outro local onde os negócios da ONDACOM estiverem sendo conduzidos. <br>
                            O assédio e a discriminação também podem ocorrer no ambiente on-line, ou por meio de comunicações eletrônicas,
                            incluindo e-mails, redes sociais, e aplicativos de mensagens. <br><br>

                            3.2) Assédio <br>
                            Significa, entre outras coisas, insistência inoportuna junto a alguém, com perguntas, propostas e pretensões.
                            Assediar, por sua vez, significa perseguir com insistência, que é o mesmo que molestar, perturbar, aborrecer,
                            incomodar, importunar. <br><br>

                            3.3) Assédio moral <br>
                            É a exposição de alguém a situações humilhantes e constrangedoras, repetitivas e prolongadas, atingindo sua honra
                            e integridade psíquica. <br><br>

                            3.3.1. Assédio moral no ambiente de trabalho <br>
                            Exige que as situações descritas aconteçam no ambiente de trabalho
                            ou em ambientes em que a vítima esteja em decorrência do trabalho. <br>
                            Manifesta-se por meio de gestos, palavras (orais ou escritas), comportamentos ou atitudes que exponham o(a)
                            empregado(a) ou o(a) estagiário(a) ou o(a) trabalhadores terceirizado(a), individualmente ou em grupo,
                            a situações humilhantes e constrangedoras, degradando o clima de trabalho e muitas vezes impactando a estabilidade
                            emocional e física da vítima. <br>
                            Para que seja caracterizado é necessário que haja atos repetitivos e prolongados no ambiente de trabalho. <br>
                            Situações isoladas podem causar dano moral, mas não necessariamente configuram assédio. <br><br>

                            O assédio moral no ambiente de trabalho pode se dar por duas formas: <br><br>

                            HORIZONTAL A vítima e o agressor são pessoas que pertencem ao mesmo nível de hierarquia,
                            com cargos ou funções sem relação de subordinação. <br><br>

                            VERTICAL A vítima e o agressor são de níveis hierárquicos diferente, chefes e subordinados,
                            e pode se manifestar de forma ascendente (de baixo para cima) ou descendente (de cima para baixo). <br><br>

                            Situações que podem ser consideradas assédio moral:

                            <ul style="list-style-type: none;">
                                <li>* Retirar a autonomia do colaborador ou contestar, a todo momento suas decisões;</li>
                                <li>* Sobrecarregar o colaborador com novas tarefas ou retirar o trabalho que habitualmente competia a ele executar, provocando a sensação de inutilidade e de incompetência;</li>
                                <li>* Ignorar a presença do assediado, dirigindo-se apenas aos demais colaboradores;</li>
                                <li>* Passar tarefas humilhantes;</li>
                                <li>* Gritar ou falar de forma desrespeitosa;</li>
                                <li>* Espalhar rumores ou divulgar boatos ofensivos a respeito do colaborador;</li>
                                <li>* Não levar em conta problemas de saúde;</li>
                                <li>* Criticar a vida particular da vítima;</li>
                                <li>* Atribuir apelidos pejorativos;</li>
                                <li>* Impor punições vexatórias;</li>
                                <li>* Postar mensagens depreciativas em grupos nas redes sociais;</li>
                                <li>* Evitar comunicação direta, dirigindo-se à vítima apenas por e-mail, bilhetes, ou terceiros e outras formas de comunicação mista;</li>
                                <li>* Isolar fisicamente o colaborador para que não haja comunicação com os demais colegas;</li>
                                <li>* Desconsiderar ou ironizar, injustificadamente as opiniões da vítima;</li>
                                <li>* Retirar cargos e funções sem motivo justo;</li>
                                <li>* Impor condições e regras de trabalho personalizadas diferentes das que são cobradas de outros profissionais;</li>
                                <li>* Delegar tarefas impossíveis de serem cumpridas ou determinar prazos incompatíveis para finalização de um trabalho;</li>
                                <li>* Manipular informações deixando de repassa-las com a devida antecedência necessária para que o colaborador realize suas atividades;</li>
                                <li>* Vigilância excessiva;</li>
                                <li>* Limitar o número de vezes que o colaborador vai ao banheiro e monitorar o tempo que ele permanece lá;</li>
                                <li>* Advertir arbitrariamente;</li>
                                <li>* Instigar o controle de um colaborador por outro, criando um controle fora do contexto da estrutura hierárquica para gerar desconfiança e evitar a solidariedade entre colegas;</li>
                                <li>* Não levar em conta recomendações de ordem médica indicadas pelo médico do trabalho;</li>
                                <li>* Induzir a vítima ao erro;</li>
                                <li>* Privá-la de acesso aos instrumentos de trabalho: telefone, computador, internet etc;</li>
                                <li>* Pressioná-la para que não faça valer seus direitos;</li>
                                <li>* Agir de modo a impedir que obtenha promoção;</li>
                                <li>* Utilizar insinuações desdenhosas para desqualificar a pessoa;</li>
                                <li>* Fazer gestos de desprezo, tais como suspiros, olhares desdenhosos etc.;</li>
                                <li>* Desacreditar o (a) trabalhador (a) diante de colegas, superiores ou subordinados;</li>
                                <li>* Espalhar rumores a respeito do (a) trabalhador (a), atribuindo-lhe problemas psicológicos.</li>
                            </ul>


                            3.3.2. O que é não é assédio moral <br>
                            É importante destacar que os conflitos fazem parte das relações humanas e de trabalho; por isso,
                            nem toda situação de atrito ou discordância constitui assédio moral. <br>
                            Além disso, alguns atos são inerentes ao trabalho de gestão de um departamento, serviço ou empresa, e,
                            quando são pontuais ou moderados, não configuram assédio moral, por exemplo: <br>

                            <ul style="list-style-type: none;">
                                <li>* Cobranças de trabalho, realizadas de maneira respeitosa;</li>
                                <li>* Atribuição de tarefas aos subordinados, no interesse da empresa;</li>
                                <li>* Conflitos esporádicos com colegas ou chefias;</li>
                                <li>* Divergências sobre determinado tema comunicadas de forma direta e respeitosa;</li>
                                <li>* Críticas construtivas;</li>
                                <li>* Avaliações de desempenho realizadas por colegas ou superiores, desde que não seja feita de forma a causar situação vexatória na pessoa avaliada;</li>
                                <li>* Exigir que o trabalho seja cumprido com eficiência e estimular o cumprimento de metas.</li>
                            </ul>


                            3.4) Assédio sexual <br>
                            Conduta de conotação sexual praticada contra a vontade de alguém, sob forma verbal, não verbal ou física,
                            manifestada por palavras, gestos, contatos físicos ou outros meios, com o efeito de perturbar ou constranger a pessoa,
                            afetar sua dignidade ou de lhe criar um ambiente intimidativo, hostil, degradante, humilhante e desestabilizador.
                            No caso do assédio sexual não há exigência que conduta seja reiterada, sendo possível a caracterização por um ato único. <br>
                            A conduta pode configurar assédio sexual mesmo quando não é motivada por desejo sexual, além de não depender do gênero sexual,
                            orientação sexual, ou expressão de gênero da vítima, que podem ser de mesmo gênero sexual, ou diferente do assediador.
                            O assédio sexual pode existir independentemente da posição do assediador ou da vítima na empresa. <br><br>

                            Exemplos de condutas que podem ser consideradas assédio sexual:

                            <ul style="list-style-type: none;">
                                <li>* Fazer críticas ou brincadeiras sobre particularidades físicas ou sexuais;</li>
                                <li>* Seguir, espionar e/ou realizar abordagem com intuito sexual;</li>
                                <li>* Insinuar ou agredir com gestos ou propostas sexuais;</li>
                                <li>* Realizar conversas indesejáveis sobre sexo;</li>
                                <li>* Constranger com piadas ou expressões de conteúdo sexual e frases de duplo sentido;</li>
                                <li>* Realizar insinuações, explícitas ou veladas, de caráter sexual;</li>
                                <li>* Realizar contato físico de forma inoportuna, tocar ou criar situações de contato corporal, sem consentimento recíproco, com conotação sexual;</li>
                                <li>* Realizar convites impertinentes e/ou pressionar para participar de encontros e saídas com intuito sexual;</li>
                                <li>* Fazer ameaças de perdas significativas ou promessas de obtenção de benefícios em troca de favores sexuais;</li>
                                <li>* Fazer chantagem para permanência ou promoção no cargo ou emprego, inclusive de empregados terceirizados;</li>
                                <li>* Fazer ameaças, veladas ou explícitas, de represálias, perturbação, ofensa, caso não receba o favor sexual;</li>
                                <li>* Contato físico não solicitado e além do formal, como toques, beijos, carícias, tapas e abraços;</li>
                                <li>* Promessas de tratamento diferenciado.</li>
                            </ul>

                            Não são desculpas para assédio sexual frases do tipo: “eu estava brincando”, “eu não quis dizer desse jeito”,
                            “isso é aceitável de onde eu venho”, ou ninguém nunca reclamou deste comportamento antes”,
                            tampouco estar sob a influência de álcool ou de outras substâncias. <br><br>

                            O silencio da vítima não pode ser considerado como aceitação da conduta sexual,
                            indesejada, e nem desconfigura o assédio sexual no trabalho.
                            A não consumação do objetivo do assediador também não descaracteriza o assédio praticado. <br><br>

                            3.4.1 O que diz a lei penal sobre o assédio sexual <br><br>
                            O Código Penal brasileiro, ao capitular a conduta que escreve o assédio faz a distinção
                            entre duas modalidades: vertical e horizontal. <br><br>

                            * Assédio sexual vertical: Ocorre quando uma pessoa se vale da sua condição de superioridade hierárquica ou de
                            ascendência inerentes ao exercício de cargo ou função para constranger alguém com objetivo de obter vantagem ou
                            favorecimento sexual. <br>
                            Essa forma clássica de assédio é caracterizada como crime, e aparece descrita no art. 216-A do Código Penal,
                            punido com detenção, de 1 (um) a 2 (dois) anos. <br><br>

                            *Assédio sexual horizontal: Ocorre quando não há distinção hierárquica entre a pessoa que assedia
                            e aquela que é assediada, a exemplo do constrangimento verificado entre colegas de trabalho. <br>
                            O assédio sexual horizontal não é “crime de assédio” previsto no Código Penal brasileiro, no artigo 216,
                            mas o autor pode ser enquadrado por importunação sexual previsto no art. 215-A,
                            punido com reclusão, de 1 (um) a 5 (cinco) anos, se o ato não constitui crime mais grave. <br><br>

                            3.4.2. O que não é assédio sexual <br>
                            Não é considerado assédio sexual, apesar de serem consideradas inadequadas no ambiente de trabalho ou até configurarem outros crimes, ou contravenções:

                            <ul style="list-style-type: none;">
                                <li>* Elogios sem conteúdo sexual;</li>
                                <li>* Cantadas;</li>
                                <li>* Paqueras ou flertes não correspondidos.</li>
                            </ul>

                            3.5) Discriminação <br>
                            É a negação injusta da igualdade de tratamento, e oportunidade a indivíduos ou grupos com base em uma ou mais
                            das características listadas abaixo, relacionadas aos termos, condições, ou privilégios de empregos,
                            incluindo, mas não limitados a contratação, demissão, promoção, disciplina, treinamentos,
                            ou decisão de remuneração:

                            <ul style="list-style-type: none;">
                                <li>* Raça, cor, origem étnica, ou nacional;</li>
                                <li>* Sexo, incluindo gravidez, parto ou amamentação;</li>
                                <li>* Orientação sexual;</li>
                                <li>* Gênero e expressão de gênero;</li>
                                <li>* Idade;</li>
                                <li>* Religião e crenças religiosas;</li>
                                <li>* Opinião política;</li>
                                <li>* Origem social;</li>
                                <li>* Incapacidade física ou mental;</li>
                                <li>* Status parental.</li>
                            </ul>

                            <p>Esta lista não é exaustiva e pode haver outras razões que atentem contra o reconhecimento ou exercício, em condições de igualdade, dos direitos e liberdades fundamentais no campo econômico, cultural, laboral, etc.</p>

                            <ul style="list-style-type: none;">
                                <li>* Pagar salário menor a uma mulher que desempenhe as mesmas funções de um homem;</li>
                                <li>* Não contratar ou não promover uma pessoa em razão de sua cor, deficiência, idade, orientação sexual ou gênero;</li>
                                <li>* Ofender ou agredir em razão de sua orientação sexual ou identidade de gênero (homofobia e transfobia);</li>
                                <li>* Isolar ou excluir pessoas com deficiência de espaços sem acessibilidade;</li>
                                <li>* Não contratar, não promover, exonerar mulheres de cargos de chefia por motivos de gravidez ou licença maternidade;</li>
                                <li>* Preterir pessoas devidamente qualificadas para o trabalho em razão de sua idade.</li>
                            </ul>


                            <span class="fw-bold">4. O QUE A VÍTIMA DE ASSÉDIO OU DISCRIMINAÇÃO DEVE FAZER</span><br>
                            Por medo, constrangimento ou vergonha, a vítima costuma silenciar, contribuindo
                            para a repetição da conduta de assédio. <br>
                            Assim, algumas medidas devem ser observadas, tanto preventivas como ostensivas,
                            de modo a coibir novos atos ou mesmo a fim de prover meios para que os elementos da conduta possam ser levados
                            ao conhecimento da administração da empresa para a adoção de providências, como por exemplo:

                            <ul style="list-style-type: none;">
                                <li>* Reunir provas do assédio e da discriminação.</li>
                                <li>* Anotar, com detalhes, todas as situações de assédio e discriminação sofridas com data, hora e local, e listar os nomes dos que testemunharam os fatos.</li>
                                <li>* Buscar ajuda dos colegas, principalmente daqueles que testemunharam o fato ou que já passaram pela mesma situação.</li>
                                <li>* Buscar orientação psicológica sobre como se comportar para enfrentar tais situações.</li>
                                <li>* Comunicar a situação ao setor responsável, ao superior hierárquico do assediador ou utilizar o canal de denúncia anônima disponibilizado pela empresa.</li>
                            </ul>

                            <span class="fw-bold">5. QUEM PODE DENUNCIAR</span><br>
                            Qualquer pessoa pode denunciar, vítima, ou colega de trabalho. <br>
                            Não é necessário advogado ou procurador para apresentar uma denúncia. <br>
                            A denúncia pode ser realizada de forma escrita ou verbal, nos termos desta política. O registro da denúncia
                            pode ser feito de forma identificada ou anônima. A escolha fica a critério do denunciante. <br>
                            Nas denúncias anônimas não será registrada nenhuma informação de nome, e-mail, telefone ou outro dado
                            cadastral do denunciante. <br><br>

                            <span class="fw-bold">6. MEDIDAS DISCIPLINARES</span><br>
                            A ONDACOM, após justa, ampla e competente apuração, poderá aplicar ao assediador medidas educativas/repressiva
                            que podem ser desde a advertência, mudança de setor ou função do agressor, alterar a jornada de trabalho,
                            suspensão ou, até mesmo, dispensa por justa causa. <br>
                            Todo colaborador que cometer violação a esta política e não for submetido a pena de rescisão do contrato de
                            trabalho, deverá ser submetido a treinamento de recuperação e compliance. <br>
                            A empresa ainda promoverá, quando necessário, o encaminhamento do caso a autoridades apropriadas para
                            verificação de violação as leis vigentes. <br><br>

                            <span class="fw-bold">7. AÇÕES PREVENTIVAS</span><br>
                            No intuito de combater o assédio a discriminação a empresa adotará as seguintes medidas:

                            <ul>
                                <li>• Oferecer informação sobre o assédio moral, sexual e discriminação através da promoção de palestras, treinamentos, rodas de discussão, etc.</li>
                                <li>• Incentivar a prática de relações respeitosas no ambiente de trabalho;</li>
                                <li>• Avaliar constantemente as relações interpessoais no ambiente de trabalho, atentando para as mudanças de comportamento;</li>
                                <li>• Dispor de instância administrativa para acolher as denúncias de maneira objetiva;</li>
                                <li>• Apurar e punir as violações denunciadas.</li>
                            </ul>


                            <span class="fw-bold">8. PROIBIÇÃO DE RETALIAÇÃO</span><br>
                            É uma violação a esta Política que qualquer funcionário retalie um indivíduo que, de boa-fé, se queixou,
                            ou expressou intenção de denunciar conduta que ele acredita ser assédio ou discriminação,
                            ou auxiliou outro colega a denunciar, ou ainda que participou de processo de investigação interna ou judicial
                            relacionada a denúncia de assédio, ou discriminação, inclusive como testemunha. <br><br>

                            <span class="fw-bold">9. CANAL DE DENÚNCIA</span><br>
                            Se o colaborador da ONDACOM tiver sido submetido, ou testemunhou qualquer forma de assédio ou discriminação,
                            pode relatar o incidente através do e-mail ouvidoria@ondacom.com <br>
                            O canal de denúncia está disponível 24 (vinte e quatro) horas por dia, 7 (sete) dias por semana,
                            sendo que o relato pode ser elaborado de forma confidencial e se desejar anonimamente. <br><br>

                            <span class="fw-bold">10. INVESTIGAÇÃO</span><br>
                            A ONDACOM investigará qualquer denúncia de violação a esta política de maneira imediata, completa e imparcial,
                            cabendo ao Comitê de ética determinar quaisquer ações corretivas ou disciplinares cabíveis. <br><br>

                            <span class="fw-bold">11. RESPONSABILIDADES</span><br>

                            Esta política define padrões mínimos da ONDACOM para tratar e prevenir o assédio e a discriminação no local
                            de trabalho e todos os seus colaboradores, de qualquer nível hierárquico se obrigam ao que segue:

                            <ol type="a" style="list-style-type: lower-alpha;">
                                <li>Qualquer pessoa a serviço da ONDACOM, tem o direito de ser tratada com dignidade e respeito, bem como de receber assistência sem qualquer ameaça de assédio, e discriminação;</li>
                                <li>A ONDACOM é responsável por criar, promover e manter um ambiente seguro, que reduz as possibilidades de ocorrência de discriminação e assédio e oferecer apoio apropriado, confidencial e eficaz às vítimas;</li>
                                <li>Espera-se que todos os colaboradores da ONDACOM atuem com tolerância, sensibilidade e respeito pela diversidade, e com o compromisso pela equidade, igualdade e diversidade;</li>
                                <li>É proibida a troca de dinheiro, emprego, bens ou serviços por sexo, ou a submissão a situações humilhantes, degradantes ou de exploração;</li>
                                <li>É proibida qualquer atividade sexual usando a pressão da posição hierárquica, ou a possibilidade de prestar assistência, proteção ou serviço;</li>
                                <li>Quando um funcionário souber ou suspeitar de um caso de discriminação, assédio, abuso ou exploração sexual por um colega de trabalho, deve relatar pelo e-mail de denúncia, ou para os seus superiores, mesmo que anonimamente;</li>
                                <li>Os funcionários devem criar e manter um ambiente que impeça o assédio, o abuso e a exploração sexual e a discriminação, e promova a implementação do código de conduta;</li>
                                <li>Os gerentes e superiores hierárquicos em todos os níveis têm responsabilidade de apoiar e desenvolver sistemas que mantenham o ambiente livre de assédio e discriminação;</li>
                                <li>Não justificar nem banalizar a violência;</li>
                                <li>Não normalizar discriminação, agressões, violência sexual ou qualquer outra forma de violência.</li>
                            </ol>
