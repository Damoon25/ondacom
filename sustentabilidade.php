                <div class="container">
                    <div class="col-sm-12 text-start textPolitic">
                        <div class="mb-5">
                            <span class="fw-bold">1. OBJETIVO</span><br>
                            Esta política tem como objetivo estabelecer diretrizes e responsabilidades de cunho ambiental, social e de governança,
                            a serem observados em todos os negócios, processos e fluxo de trabalho da ONDACOM e,
                            visa reforçar dando ampla divulgação ao compromisso da empresa com a sustentabilidade. <br><br>

                            <span class="fw-bold">2. ABRANGÊNCIA</span><br>
                            Esta política se aplica no âmbito da ONDACOM, e a todos os seus colaboradores, incluindo sócios, diretores,
                            bem como fornecedores e terceiros, entrando em vigor na data da sua publicação e vigendo por tempo indeterminado,
                            podendo ser revisada, alterada a qualquer tempo, conforme a necessidade institucional. <br><br>

                            <span class="fw-bold">3. RESPONSABILIDADES</span><br>
                            São responsáveis pela gestão desta política: diretoria administrativa, gerência de compras, gerência financeira,
                            gerência jurídica, e gerência de operação. <br><br>

                            <span class="fw-bold">4. CONCEITOS</span><br>
                            Desenvolvimento sustentável: promoção do desenvolvimento, visando atender às necessidades da geração presente sem
                            afetar o atendimento às demandas das gerações futuras. <br><br>

                            “Due Diligence”: refere-se à avaliação de risco de quaisquer candidatos a fornecedor, parceiro ou cliente,
                            antes da assinatura de um contrato ou formalização de qualquer vínculo comercial, a fim de verificar se o
                            candidato apresenta alguma não conformidade que possa impactar em má reputação para a ONDACOM. <br><br>

                            Direitos humanos: direitos e garantias fundamentais, inerentes a todos os seres humanos, independentemente da sua cor,
                            gênero, nacionalidade, etnia, idioma, religião ou qualquer outra condição. Incluem o direito à vida e à liberdade,
                            liberdade de opinião e expressão, o direito ao trabalho e à educação, entre outros. <br><br>

                            Gases de efeito estufa (GEE): Os gases de efeito estufa são gases que absorvem e emitem energia radiante dentro da
                            faixa do infravermelho térmico, causando o aquecimento global e mudanças climáticas. Os principais gases de efeito de
                            estufa na atmosfera da Terra são o vapor de água, dióxido de carbono, metano, óxido nitroso e ozônio. <br><br>

                            <span class="fw-bold">5. DIRETRIZES GERAIS</span><br>
                            Todas as atividades desenvolvidas pela ONDACOM devem ser constantemente avaliadas, tendo como referência as boas
                            práticas de sustentabilidade e responsabilidade social, observando-se as seguintes diretrizes: <br><br>

                            <span class="fw-bold">5.1. DIRETRIZES DE GOVERNANÇA:</span><br>
                            <ol type="a" style="list-style-type: lower-alpha;">
                                <li>Estabelecer objetivos, indicadores, metas e responsabilidades para a gestão da sustentabilidade socioambiental;</li>
                                <li>Promover a transparência na sua gestão e na aplicação dos recursos financeiros;</li>
                                <li>Atuar em concordância com as políticas públicas associadas à responsabilidade social empresarial e ambiental, com base nos princípios éticos de direitos humanos universais;</li>
                                <li>Promover e financiar o desenvolvimento local e regional dos locais em que está instalada buscando a sustentabilidade e inclusão social;</li>
                                <li>Promover e assegurar a ética e a integridade nos negócios, processos e relacionamentos com partes interessadas, estabelecidos pelo Código de Ética da ONDACOM;</li>
                                <li>Assegurar medidas e incentivar comportamentos no combate à corrupção, suborno e lavagem de dinheiro, ancorados em um conjunto de normativas regulatórias e procedimentos internos;</li>
                                <li>Disponibilizar canais de denúncia acessíveis e adequados às partes interessadas para acolhimento e apuração de manifestações sobre suspeitas de violações referentes a ética e integridade;</li>
                                <li>Realizar o tratamento adequado de dados pessoais, inclusive nos meios digitais, para proteger os direitos fundamentais de liberdade e de privacidade;</li>
                                <li>Respeitar e cooperar com os órgãos reguladores, mantendo conformidade com as condições e normas estabelecidas;</li>
                                <li>Promover a adoção das melhores práticas de ESG por todos os departamentos da empresa;</li>
                                <li>Atuar em consonância com os tratados, acordos, pactos e convenções nacionais e internacionais sobre meio ambiente e responsabilidade social;</li>
                                <li>Combater quaisquer práticas que não estejam em conformidade legal ou que caracterizem situações de corrupção ou suborno;</li>
                                <li>Motivar os colaboradores a praticarem a responsabilidade social;</li>
                                <li>Monitorar o cumprimento das ações estabelecidas nesta política e identificar eventuais deficiências na implantação das ações;</li>
                                <li>Atuar de maneira proativa para identificar os atuais ou potenciais impactos dos negócios ONDACOM, contribuindo para que todas as nossas ações sejam vetores de desenvolvimento sustentável.</li>
                            </ol>


                            <span class="fw-bold">5.2 DIRETRIZES AMBIENTAIS</span>
                            <ol type="a" style="list-style-type: lower-alpha;">
                                <li>Desenvolver e aprimorar, de forma contínua, produtos e serviços que incorporem critérios socioambientais.</li>
                                <li>Valorizar e incentivar o uso de tecnologias limpas.</li>
                                <li>Priorizar o mercado local e regional com vista à promoção do desenvolvimento local sustentável, fortalecendo a responsabilidade socioambiental.</li>
                                <li>Contratar e adquirir produtos e serviços com observância de critérios ambientais.</li>
                                <li>Incentivar o consumo, quando cabível, de produtos reciclados, reutilizados e biodegradáveis.</li>
                                <li>Contribuir para a prevenção, mitigação e minimização dos efeitos negativos causados pela mudança de clima.</li>
                                <li>Assegurar que as operações da companhia busquem constantemente a redução das emissões de Gases de Efeito Estufa (GEE).</li>
                                <li>Praticar a gestão responsável de recursos hídricos, para assegurar a disponibilidade de água em qualidade e quantidade necessárias para o abastecimento dos territórios onde atua.</li>
                                <li>Prezar pelos princípios de preservação e restauração do meio ambiente na tomada de decisão comercial, de investimento e demais atividades da ONDACOM.</li>
                                <li>Priorizar a seleção de infraestruturas prediais com melhores práticas de gestão ambiental e bem-estar, preferencialmente com certificações.</li>
                                <li>Valorizar as infraestruturas prediais localizadas em zonas de fácil acesso, conectadas ao transporte público e que possibilitem uso de modais de transporte de baixo carbono.</li>
                                <li>Incentivar as práticas sustentáveis dentro do ecossistema da ONDACOM, apoiando nossos colaboradores, clientes, fornecedores e parceiros.</li>
                                <li>Promover a gestão dos resíduos, incluindo os eletroeletrônicos, garantindo seu descarte adequado e realizando a separação dos resíduos recicláveis, destinando-os às associações e cooperativas de catadores, quando possível.</li>
                            </ol>


                            <span class="fw-bold">5.3 DIRETRIZES SOCIAIS</span><br>
                            <ol type="a" style="list-style-type: lower-alpha;">
                                <li>Incentivar projetos que estimulam o empreendedorismo e o fortalecimento da base empresarial, principalmente das micro e pequenas empresas.</li>
                                <li>Prezar pelos Direitos Humanos e pela promoção de uma sociedade resiliente e inclusiva na tomada de decisão comercial, de investimento e demais atividades da ONDACOM.</li>
                                <li>Assegurar um ambiente de trabalho saudável, seguro, acessível e inclusivo, que respeite as legislações vigentes e alinhado sempre que possível às melhores práticas do mercado.</li>
                                <li>Incentivar hábitos saudáveis entre os colaboradores com foco na saúde física, mental e financeira.</li>
                                <li>Respeitar os Direitos Humanos, fomentar a diversidade e a inclusão, além de combater qualquer tipo de assédio e discriminação.</li>
                                <li>Garantir o direito de equidade de oportunidades e de tratamento para todos os colaboradores.</li>
                                <li>Incentivar o desenvolvimento pessoal e profissional dos colaboradores alinhado ao propósito da empresa e seus objetivos estratégicos.</li>
                                <li>Adotar uma postura ética e de diálogo com as diferentes partes interessadas com que a Empresa se relaciona, incluindo comunidades.</li>
                                <li>Promover ações e iniciativas de interesse público, por mecanismos diversos, incluindo investimento social privado, como doações, uso de recursos incentivados e programas de voluntariado envolvendo colaboradores.</li>
                                <li>Fomentar a aquisição de produtos e contratação de serviços de fornecedores inseridos nos territórios de operação da Empresa e/ou regiões que apresentem indicadores socioeconômicos mais baixos, fornecedores que tenham atuação de destaque em questões de sustentabilidade, e quando possível, pequenos e médios fornecedores.</li>
                                <li>Banir qualquer tipo de relacionamento com fornecedores e parceiros comprovadamente envolvidos em práticas de trabalho análogo à escravidão, trabalho infantil, exploração sexual de crianças e adolescentes, prostituição e tráfico de pessoas.</li>
                                <li>Adotar, em suas unidades, ações para viabilizar a acessibilidade de pessoas portadoras de deficiência ou com mobilidade reduzida em cumprimento à legislação vigente, nos níveis federal, estadual e municipal.</li>
                                <li>Apoiar projetos, programas e ações de responsabilidade socioambiental que visem minimizar desigualdades sociais, fomentar a educação e a cidadania, e proteger crianças, adolescentes e idosos em situação de risco e vulnerabilidade social.</li>
                                <li>Criar oportunidades justas para que grupos subrepresentados, como mulheres e negros, ocupem cargos de chefia na organização.</li>
                                <li>Desenvolver, implantar e manter uma política de saúde e segurança do trabalho baseada no princípio de que normas de saúde e segurança e desempenho organizacional sólidos se apoiam e se reforçam mutuamente.</li>
                            </ol>


                            <span class="fw-bold">6. MEDIDAS DISCIPLINARES</span><br>
                            O Colaborador ou Terceiro que descumprir quaisquer das determinações previstas neste documento estará sujeito
                            às sanções previstas no Código de Ética da empresa, sem prejuízo da aplicação de disciplinares, 
                            incluindo a rescisão contratual. <br><br>

                            6.1. Todos os incidentes informados de suspeitas de violação desta Política serão apurados e reportados a 
                            Diretoria da ONDACOM que deverá realizar investigação. <br><br>

                            6.2. Concluída a investigação dos fatos, caso seja constatada a ocorrência de uma conduta que 
                            infringe as regras dessa Política, serão tomadas medidas corretivas imediatas e exemplares, 
                            sempre de acordo com as circunstâncias, gravidade e a lei aplicável. <br><br>
