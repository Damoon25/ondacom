                        <p class="mb-5">
                            <span class="fw-bold">1. OBJETIVO </span><br>
                            A finalidade desta política é auxiliar todos os colaboradores e parceiros da ONDACOM a cumprir
                            estritamente as diretrizes da Lei nº 12.846/2013 (Lei Anticorrupção Brasileira) e suas regulamentações,
                            a Lei nº 8.429/1992 (Lei da Improbidade Administrativa) e outras leis nacionais e internacionais,
                            para estabelecer as principais orientações e posicionamento da  empresa relacionadas ao repúdio
                            e combate a todas as formas de condutas corruptas, tais como suborno,
                            desvios e concessões de vantagens indevidas,
                            assim como a ocultação ou dissimulação desses atos e o
                            impedimento às atividades de investigação e fiscalização.
                        </p>
                        <p class="mb-5">
                            <span class="fw-bold">2. ABRANGÊNCIA </span><br>
                            Esta política se aplica no âmbito da ONDACOM, e a todos os seus colaboradores, incluindo sócios,
                            diretores, bem como fornecedores e terceiros,
                            entrando em vigor na data da sua publicação e vigendo por tempo indeterminado,
                            podendo ser revisada, alterada a qualquer tempo, conforme a necessidade institucional. 
                             
                        </p>
                        <p class="mb-5">
                            <span class="fw-bold me-0">3. RESPONSABILIDADES </span><br>
                            São responsáveis pela gestão desta política: diretoria administrativa, gerencia de compras, gerência financeira,
                            gerência jurídica, e gerência de operação.
                             
                        </p>
                        <p>
                            <span class="fw-bold">4. DEFINIÇÕES</span><br><br>
                            <span class="fw-bold text-decoration-underline">Colaborador:</span> todos os empregados, administradores, diretores,
                            aprendizes e estagiários, independentemente de cargo ou função exercidos. <br><br>
                            <span class="fw-bold text-decoration-underline">Membro Próximo:</span>significa e inclui, com relação a uma pessoa,
                            seu cônjuge ou companheiro, consanguíneo ou afim, em linha reta (ascendente ou descendente) ou colateral,
                            até o segundo grau de parentesco. <br><br>
                            <span class="fw-bold text-decoration-underline">Terceiro:</span> toda pessoa física que não seja colaborador,
                            ou pessoa jurídica que seja contratada ou subcontratada para representar ou atuar em nome da ONDACOM. <br><br>
                            <span class="fw-bold text-decoration-underline">Agentes públicos:</span> qualquer funcionário público, agente político,
                            servidor público e empregado público, pertencente à Administração Pública direta e indireta de qualquer dos Poderes
                            da União, dos Estados, do Distrito Federal, dos Municípios, de empresa incorporada ao patrimônio público
                            ou de entidade para cuja criação ou custeio o erário haja concorrido,
                            ou concorra com mais de cinquenta por cento do patrimônio ou da receita anual,
                            ainda que em exercício transitório de cargo ou função e sem remuneração. <br><br>
                            <span class="fw-bold text-decoration-underline">Vantagem indevida:</span> qualquer benefício de valor monetário ou não, ainda que de valor apenas para o beneficiário,
                            que não é devido por força de uma obrigação legal, contratual ou em decorrência do exercício de cargo,
                            emprego ou função, com a finalidade de obtenção ou manutenção de negócios, ou qualquer outro benefício
                            ou vantagem impróprios, tangível ou intangível, para a Companhia.
                            A definição de Vantagem Indevida deve ser interpretada de modo amplo, podendo compreender, por exemplo: <br><br>

                        <ul>
                            <li>• Pagamentos ou doações em produtos ou dinheiro;</li>
                            <li>• Oportunidades de negócios;</li>
                            <li>• Favores;</li>
                            <li>• Brindes ou presentes;</li>
                            <li>• Entretenimento, inclusive ingressos para shows e eventos;</li>
                            <li>• Viagens, passagens aéreas, estadias ou refeições;</li>
                            <li>• Fornecimento gratuito de produtos ou com descontos especiais;</li>
                            <li>• Oferta de emprego, inclusive estágio, remunerado ou não, para Funcionário Público ou pessoas próximas a ele.</li>
                        </ul>


                        Compliance: o processo sistemático e contínuo que visa garantir o cumprimento das legislações vigentes,
                        políticas e diretrizes estabelecidas para o negócio, com o objetivo de prevenir,
                        detectar e tratar qualquer desvio de conduta identificado ou ato de Corrupção,
                        e promover uma cultura organizacional baseada na ética e na transparência. <br><br>

                        Corrupção: é o ato de considerar prometer, oferecer, dar, direta ou indiretamente,
                        ou ainda solicitar, receber ou aceitar, vantagem indevida a Agente Público, Agente Privado,
                        ou terceiro por eles indicado, para influenciá-los a fazer algo que é desonesto ou ilegal,
                        causando uma ruptura com a ordem legal em benefício de alguém, para obter,
                        manter ou proporcionar negócios ou benefícios relevantes, ou comprovadamente financiar,
                        custear, patrocinar ou de qualquer modo subvencionar essas práticas. São formas de corrupção: (a) Corrupção Ativa:
                        é o ato de oferecer ou prometer Vantagem Indevida a Agente Público, para determiná-lo a praticar,
                        omitir ou retardar ato de ofício; e (b) Corrupção Passiva: é o ato de solicitar ou receber,
                        para si ou para outrem, direta ou indiretamente, ainda que fora da função ou antes de assumi-la,
                        mas em razão dela, Vantagem Indevida, ou aceitar promessa de tal vantagem. <br><br>

                        <span class="fw-bold text-decoration-underline">Fraude:</span> ato ilícito ou de má-fé que visa à obtenção de vantagens
                        indevidas ou majoradas, para si ou para terceiros, geralmente pelo cometimento de crimes ou por omissões, inverdades,
                        abuso de poder, quebra de confiança, burla de regras, dentre outros. <br><br>

                        <span class="fw-bold text-decoration-underline">Improbidade Administrativa:</span> é o ato ilegal ou contrário aos
                        princípios básicos da administração pública, cometido por Agente Público durante o exercício de função pública
                        ou decorrente desta, nos termos descritos nas Leis Anticorrupção. <br><br>

                        <span class="fw-bold text-decoration-underline">Leis Anticorrupção:</span> são os seguintes atos normativos
                        brasileiros e estrangeiros, aplicáveis à ONDACOM: (i) Lei n° 8.137/1990
                        (“Lei dos Crimes contra a Ordem Econômica”); (ii) Lei nº 8.429/1992
                        (“Lei de Improbidade Administrativa”); (iii) Lei n° 8.666/1993 (“Lei de Licitações”);
                        (iv) Lei nº 12.813/2013 (“Lei de Conflito de Interesses”); (v) Lei n° 12.846/2013,
                        regulamentada pelo Decreto n° 8.420/2015 (“Lei Anticorrupção Brasileira”);(vii)
                        Lei Norte-Americana sobre Práticas de Corrupção no Exterior (“FCPA – Foreign Corrupt Practices Act”). <br><br>

                        <span class="fw-bold text-decoration-underline">Tráfico de Influência:</span> é o ato de solicitar, exigir,
                        cobrar ou obter, para si ou para outrem, vantagem ou promessa de vantagem, a pretexto de influir em ato praticado
                        por Agente Público no exercício da função. <br><br>

                        Intermediários: qualquer representante e/ou contratado da ONDACOM que tenha ou possa vir a ter contato
                        com Autoridades Governamentais ou Funcionários Públicos em nome e/ou no interesse ou benefício da ONDACOM,
                        ou que represente ou possa vir a representar a empresa perante Entidades Governamentais e/ou Funcionários Públicos.
                        <br><br>
                        </p>

                        <span class="fw-bold">5. DIRETRIZES GERAIS </span><br><br>
                        A ONDACOM não tolera atos corruptivos em nenhuma hipótese, incluindo pagamentos de facilitação,
                        no Setor Público, no Setor Privado ou no Terceiro Setor, sem qualquer distinção. Desta forma,
                        todos as pessoas físicas ou jurídicas diretas,
                        ou indiretamente relacionadas a empresa devem se atentar para as seguintes diretrizes gerais: <br><br>

                        5.1. É dever de todos os Colaboradores e parceiros da ONDACOM conduzir negócios com integridade,
                        por meio de condutas éticas, transparentes, honestas e legítimas; <br><br>

                        5.2. Os Colaboradores e Terceiros relacionados a ONDACOM estão proibidos de oferecer e/ou conceder,
                        a qualquer Agente Público ou Privado, qualquer Vantagem Indevida, monetária ou não, ou praticar Tráfico de Influência
                        com o objetivo de influenciar as decisões que afetem os negócios da empresa;
                        ou obter um ganho pessoal que possa causar algum impacto nos interesses da companhia;
                        ou para obter informações confidenciais sobre oportunidades de negócios,
                        licitações ou as atividades de seus concorrentes; <br><br>

                        5.3. Todos os terceiros e parceiros de negócio que de alguma maneira se relacionarem com a ONDACOM devem respeitar
                        as leis e regulamentos, locais e nacionais estrangeiros de combate a corrupção; <br><br>

                        5.4. São vedadas quaisquer contratações ou celebração de parcerias com pessoa física ou jurídica de duvidosa reputação
                        ou mantenha conduta incompatível com princípios éticos praticados pela ONDACOM; <br><br>

                        5.5. A ONDACOM não tolera que um Funcionário ou Intermediário sofra consequências adversas por se recusar a
                        (i) oferecer, prometer, pagar, dar ou autorizar uma Vantagem Indevida a um Funcionário Público, ou (ii)
                        frustrar, fraudar ou manipular procedimentos licitatórios públicos e/ou contratos públicos,
                        mesmo que isso resulte na perda de oportunidades de negócios para a empresa; <br><br>

                        5.6. O Funcionário ou Intermediário não será penalizado por conduta que viole esta
                        Política caso se encontre sob iminente ameaça à sua vida ou segurança ou de terceiros.
                        Tal conduta deve, no entanto, ser comunicada imediatamente diretoria administrativa e área de compliance; <br><br>

                        5.7. Para que um pagamento ou coisa de valor viole a Legislação Anticorrupção, não é necessária a comprovação
                        de intenção de corromper nem que a obtenção do benefício ou vantagem supostamente pretendidos tenha se concretizado;
                        <br><br>

                        5.8. A ONDACOM não autoriza quem quer que seja a, em seu nome, oferecer qualquer tipo de vantagem indevida a
                        agentes públicos com o objetivo de apressar ou viabilizar a obtenção de licenças, autorizações e permissões; <br><br>

                        5.9. Não é permitido realizar pagamento para obtenção de qualquer tipo de vantagem indevida.
                        Essa diretriz se aplica a quem incentivar ou executar os pagamentos, considerando (sem limitação): <br>

                        <ul>
                            <li>a) Aprovar o pagamento de suborno;</li>
                            <li>b) Fornecer ou aceitar faturas falsas;</li>
                            <li>c) Retransmitir instruções para pagamento de suborno;</li>
                            <li>d) Encobrir o pagamento de suborno;</li>
                            <li>e) Cooperar conscientemente com o pagamento de suborno;</li>
                            <li>f) Pagar, mesmo que com recursos próprios, por atos ilícitos.</li>
                        </ul>

                        <p>5.10. É vedada expressamente a realização em nome da ONDACOM de:</p>

                        <ul>
                            <li>a) Promessa direta ou indireta, oferta, ou autorização de qualquer negociação
                                que envolva valores sem a devida especificação de sua origem, destinação e autorização;</li>
                            <li>b) Oferta ou recebimento de qualquer comissão, empréstimo, honorário ou recompensa;</li>
                            <li>c) Pagamentos sem vínculos contratuais, que não tenham a ver com o negócio ou que sejam excessivos;</li>
                            <li>d) Negociações que envolvam preços muito baixos visando o beneficiamento de terceiros ou da própria ONDACOM;</li>
                            <li>e) Subsídios pagos a outros, como bolsas de estudo; viagens ou emprego para familiares de terceiros ou partes
                                relacionadas;</li>
                            <li>f) Oferecer ajuda, doações ou votos destinados a exercer influência inadequada.</li>
                        </ul>

                        5.11. Os atos de corrupção podem envolver funcionários públicos ou entidades governamentais bem como familiares
                        próximos e associados comerciais destes; <br><br>

                        5.12. Todos os empregados da ONDACOM, órgãos de Governança, membros diretivos, terceiros e/ou fornecedores têm a
                        obrigação de comunicar qualquer violação suspeita ou real ao código de conduta por qualquer
                        que se caracterizem como atos de corrupção e suborno. <br><br>

                        5.13. Todos os desvios relacionados à fraude e corrupção, via canal de denúncias, possuem total sigilo,
                        confidencialidade e proteção aos denunciantes. <br><br>

                        5.14. É expressamente vedado qualquer ato que dificulte ou impeça a atuação dos órgãos reguladores e fiscalizadores,
                        entidades e agentes públicos. <br><br>

                        5.15. As contratações de Terceiros (fornecedores, intermediários, consultores, despachantes, dentre outros),
                        Colaboradores, inclusive as contrapartes em operações societárias realizadas pela ONDACOM, serão submetidas à análise prévia
                        por meio do “Processo de Due Diligence de Terceiros”
                        realizado pela área de Controles Internos, em que serão averiguadas informações constantes de questionários
                        ou de dados públicos que, se demonstrarem indícios de condutas indevidas,
                        ou de riscos para a execução do contrato, as quais serão consideradas “red flags”.
                        Neste caso, a área de Controles Internos/Compliance apontará eventuais riscos e dará sua recomendação acerca da
                        contratação ou não ou, ainda, sobre a permanência da relação, ficando a cargo da área de negócio a tomada
                        da decisão final e justificada acerca da contratação. <br><br>

                        5.16. São exemplos de sinais de alertas a serem identificados pelos colaboradores: <br>

                        <ul>
                            <li>• CNAE (Classificação Nacional de Atividade Econômica) diferente do item e/ou serviço a ser comprado/prestado.</li>
                            <li>• Estruturas de contratação que causem estranheza por sua complexidade.</li>
                            <li>• Despesa de viagem ou Presentes envolvendo Agentes Públicos.</li>
                            <li>• Pedido para a Companhia usar um Terceiro indicado pelo Agente Público.</li>
                            <li>• Valor aparentemente alto para o serviço em questão.</li>
                            <li>• Sugestão de pagamento em dinheiro ou para contas não identificadas ou no estrangeiro.</li>
                            <li>• Pagamento por serviços que, aparentemente, não foram prestados.</li>
                            <li>• Empresa contratada pertence a um Agente Público ou Pessoa Próxima.</li>
                            <li>• Mesma informação de contato para diferentes empresas.</li>
                            <li>• Documentação de suporte não corresponde ao pedido de pagamento.</li>
                            <li>• Má reputação do Terceiro no mercado.</li>
                            <li>• Valor excessivo a título de comissão.</li>
                            <li>• Falta de conhecimento técnico e/ou recursos para desempenho das funções contratadas.</li>
                            <li>• Operações com Pessoas Politicamente Expostas – PPE.</li>
                            <li>• Recusa a incluir cláusulas anticorrupção no contrato.</li>
                            <li>• Agentes autônomos, sem escritório ou empregados.</li>
                        </ul>


                        <span class="fw-bold">6. DIRETRIZES DE RELACIONAMENTO COM TERCEIROS </span><br>
                        É política da ONDACOM fazer negócios com parceiros de negócio que tenham reputação e integridade ilibadas,
                        cabendo às respectivas pessoas ou entidades a comprovação da idoneidade, sem prejuízo da possibilidade
                        de a Companhia realizar due diligence; <br><br>

                        6.1 A ONDACOM não admite que qualquer parceiro de negócio exerça qualquer tipo de influência imprópria em benefício
                        da empresa sobre qualquer pessoa, seja ela agente público ou não; <br><br>

                        6.2 Em todos os contratos firmados com parceiros de negócio, é obrigatória a inclusão da cláusula anticorrupção.<br><br>

                        6.3. A ONDACOM poderá, no curso da execução contratual e à critério da Comissão de Gestão de riscos de contratos,
                        efetuar a avaliação do parceiro de negócios realizando due diligence que se mostrarem convenientes ao cumprimento
                        do ajuste contratual. Os critérios de avaliação serão balizados pelas melhores práticas de governança corporativa
                        e compliance e poderão variar de acordo com o porte da empresa e a importância ou vulto do negócio jurídico.<br><br>

                        6.4. A ONDACOM não admite nenhuma prática de corrupção por parte de parceiros de negócio que atuam em seu nome,
                        mesmo que informalmente. <br><br>

                        <span class="fw-bold">
                            7. PRESENTES, BRINDES, ENTRETENIMENTO, REFEIÇÕES E DESPESAS COM VIAGEM PARA FUNCIONÁRIOS PÚBLICOS E
                            AUTORIDADES PÚBLICAS
                        </span><br><br>

                        7.1 Os Funcionários e Intermediários da ONDACOM estão proibidos de prometer, oferecer,
                        dar ou autorizar qualquer presente, brinde, entretenimento, refeição,
                        pagamento de despesas de viagem ou outro tipo de hospitalidade para Autoridades Governamentais
                        ou Funcionários Públicos sem aprovação prévia, por escrito,
                        da gerência responsável por ética e compliance da empresa. <br><br>

                        7.2. Somente serão autorizados pela gerência responsável pela ética e compliance da empresa Presentes e
                        Hospitalidade a Autoridade Governamentais ou Funcionários Públicos que: <br>

                        <ul>
                            <li>• Sejam destinados a propósitos legítimos e adequados, vinculados às atividades fim da Companhia;</li>
                            <li>• Não estejam vinculados a qualquer retribuição ou contrapartida pretendida;</li>
                            <li>• Sejam limitados e razoáveis em valor, tipo e quantidade;</li>
                            <li>• Sejam permitidos pelas regras internas do seu receptor;</li>
                            <li>• Sejam entregues abertamente ao receptor (e não de maneira escondida);</li>
                            <li>• Não constranjam a ONDACOM se divulgados publicamente;</li>
                            <li>• Não contrariem ou violem a Legislação Anticorrupção ou as regras e procedimentos estabelecidos nesta Política.</li>
                        </ul>


                        7.3. Para todas as despesas incorridas com Presentes e Hospitalidades para Autoridades Governamentais e
                        Funcionários Públicos devem ser emitidos os respectivos documentos de suporte.
                        Referidas despesas devem ser identificadas com precisão e ter sua documentação de suporte
                        (recibos, notas fiscais etc.) devidamente arquivada e registrada nos livros e registros contábeis da Companhia. <br><br>

                        <span class="fw-bold">8. ENTREGA E OFERTA DE BRINDES CORPORATIVOS</span><br>
                        8.1 Brindes corporativos não poderão ter como objetivo a interferência na decisão de Agente Público ou Privado. <br><br>

                        8.2. A entrega ou oferta de brindes corporativos deverá ter propósito claro e transparente,
                        devendo ser praticada com razoabilidade e proporcionalidade,
                        preservando a imparcialidade das relações e a reputação e a imagem da ONDACOM. <br><br>

                        8.3. Os brindes corporativos deverão estar claramente vinculados a ações de marketing institucional,
                        não deverão ter valor comercial, isto é, deverão ser brindes institucionais
                        (exemplos: canetas, camisetas, livros institucionais) <br><br>

                        8.4. Na oferta de brindes corporativos, é proibido qualquer: <br>
                        <ul>
                            <li>a) Transação em dinheiro ou similares (como, por exemplo, vale-presente), independentemente do valor.</li>
                            <li>b) Brinde com a intenção de influenciar ato ou decisão do Agente Público ou Privado.</li>
                            <li>c) Atitude que configure um conflito de interesses, real ou aparente.</li>
                        </ul>


                        <span class="fw-bold">9. CONTRIBUIÇÕES A PARTIDOS POLÍTICOS E CAMPANHAS ELEITORAIS</span><br>
                        A ONDACOM e suas subsidiárias são proibidas de realizar contribuições políticas de qualquer natureza,
                        mesmo em países ou regiões em que essas contribuições sejam permitidas pela legislação local. <br><br>

                        9.1. Por contribuições políticas entendem-se quaisquer tipos de doações feitas direta ou indiretamente a
                        partidos políticos, candidatos a cargos públicos eletivos ou seus representantes,
                        sejam tais doações realizadas em períodos eleitorais ou não. <br><br>

                        9.2. Doações compreendem, mas não se limitam, a valores em dinheiro, produtos, presentes, brindes, viagens, ingressos,
                        entretenimentos e bens de qualquer espécie, independentemente de seu valor. <br><br>

                        9.3. Entende-se como contribuição política indireta aquela em que um diretor da ONDACOM ou funcionário
                        forneça recursos para ou solicite que um terceiro faça uma contribuição política em seu lugar. <br><br>

                        <span class="fw-bold">10. REGISTROS CONTÁBEIS.</span><br>
                        A ONDACOM estabelece e mantem controles internos que garantem que: <br>
                        <ul>
                            <li>
                                a) Todas as despesas e operações envolvendo pagamentos são aprovadas segundo suas normas de governança e
                                alçadas de aprovação.
                            </li>
                            <li>
                                b) Todas as operações são registradas de maneira a permitir a elaboração das demonstrações
                                financeiras de acordo com os princípios contábeis vigentes.
                            </li>
                        </ul>

                        <span class="fw-bold">11. OPERAÇÕES SOCIETÁRIAS E CONSÓRCIOS</span><br>
                        A ONDACOM tem o compromisso de realizar due diligence adequada e razoável em processos que envolvam operações
                        societárias (como, por exemplo, fusões e aquisições) e formação de consórcios e associações a fim de verificar
                        a ausência do cometimento de possíveis irregularidades ou ilícitos, para fins desta Política,
                        pelas pessoas jurídicas envolvidas. <br><br>

                        <span class="fw-bold">12. DA PARTICIPAÇÃO EM LICITAÇÕES</span><br>
                        Ao participarem em licitações públicas, os Sócios ou Colaboradores da ONDACOM, bem como as pessoas físicas e jurídicas
                        externas que estejam atuando no interesse ou em benefício da empresa, deverão observar o seguinte: <br>

                        <ul>
                            <li>12.1 Não oferecer Vantagem Indevida, direta ou indiretamente, a Agente Público, ou a terceira pessoa a ele relacionada</li>
                            <li>12.2 Não simular competição com outras empresas</li>
                            <li>12.3 Não auxiliar na elaboração de documentos da licitação de responsabilidade do órgão contratante</li>
                            <li>12.4 Não frustrar o caráter competitivo da licitação</li>
                            <li>12.5 Fornecer informações precisas a clientes e potenciais clientes, sobre os produtos e serviços da ONDACOM</li>
                            <li>12.6 Compartilhar documentos técnicos com clientes ou potenciais clientes somente quando necessário</li>
                            <li>12.7 Oferecer e vender produtos e serviços da ONDACOM que atendam às reais necessidades dos clientes</li>
                            <li>12.8 Proibido adotar qualquer conduta ilegal, antiética ou imoral em relação a Agentes Públicos</li>
                        </ul>

                        <ul>
                            <li>a) Frustrar ou fraudar o caráter competitivo de licitação pública</li>
                            <li>b) Afastar ou procurar afastar licitante por meio de fraude ou oferecimento de vantagem</li>
                            <li>c) Criar pessoa jurídica de modo fraudulento para participar de licitação pública</li>
                            <li>d) Obter benefício escuso de modificações ou prorrogações de contratos com a administração pública</li>
